class Powers {

    public static void main(String args[]){
        double n = 3.5;

        System.out.println("The number is = " + n);

        System.out.println(n + " squared = " + n * n);
        System.out.println(n + " cubed = " + n * n * n);
        System.out.println(n + " to the fourted = " + Math.pow(n, 4));
    }
}
